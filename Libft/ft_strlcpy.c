#include <stdio.h>
#include <stdlib.h>
#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (src[i] && i < size - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i])
		i++;
	return (i);
}

int	main(void)
{
	char	bishal[] = "hello";
	char	dest[8];

	ft_strlcpy(dest, bishal, 7);
	printf("%s\n", dest);
	return (0);
}